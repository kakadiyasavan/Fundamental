public class varia {

    public static void main(String[] args) {
        int a = 10;
        String b = "Hello";
        boolean c = true;
        float d = 69.8f;
        long e = 155;
        char h = 'j';

        System.out.println("A = " + a);
        System.out.println("B = " + b);
        System.out.println("C = " + c);
        System.out.println("D = " + d);
        System.out.println("E = " + e);
        System.out.println("F = " + h);
    }
}
